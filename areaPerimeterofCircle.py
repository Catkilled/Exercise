



#area & perimeter of circle

print("Area & Perimeter of Circle")

pi = 3.14
print("Pi value = ", pi)

diameter = int(input())
print("DIameter = ", diameter)

radius = diameter/2
print("Radius = ", radius)

area = pi*radius**2
perimeter = 2*pi*radius

print("Area of Circle = ", area)
print("Perimeter of Circle = ", perimeter)








